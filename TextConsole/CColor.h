static inline void colorToRGB(uint32_t color, CRGBColor& rgb) {
    unsigned int c = static_cast<unsigned int>(color);
    rgb.red = (c >> 16) & 0xff;
    rgb.green = (c >> 8) & 0xff;
    rgb.blue = c & 0xff;
}

static inline void colorToRGB(CColor color, CRGBColor& rgb) {
    return colorToRGB(static_cast<uint32_t>(color), rgb);
}

